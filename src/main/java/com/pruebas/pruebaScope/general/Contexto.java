package com.pruebas.pruebaScope.general;

public class Contexto {
	
	private String nombre;
	
	
	public Contexto(String nombre) {
		super();
		this.nombre = nombre;
	}


	public Contexto() {
		super();
	}


	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}




}
